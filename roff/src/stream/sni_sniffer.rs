pub struct TlsHelperStream<T> {
    inner: BufStream<T>,
    buf: smallvec::SmallVec<[u8; 512]>,
}
fn cut(data: &[u8], range: Range<usize>) -> Result<&[u8], &'static str> {
    let len_in_bytes = data
        .get(range.clone())
        .ok_or("failed to get data  length field from range")?;
    let mut len = 0usize;
    for b in len_in_bytes {
        len += len << 8 | (*b as usize)
    }
    return data
        .get(range.end..range.end + len)
        .ok_or("failed to slice data");
}

fn truncate_before(data: &[u8], range: Range<usize>) -> Result<&[u8], &'static str> {
    let len = cut(data, range.clone())?.len();
    Ok(&data[range.end + len..])
}

#[derive(Serialize, Deserialize, Default, Clone, Debug, PartialEq)]
pub struct TlsRecord {
    pub suites: Option<Vec<CipherSuite>>,
    pub sni: Option<SmallString>,
    pub version: Option<TlsVersion>,
    pub trusted_root_ca_file: Option<String>,
    pub trusted_root_ca_contents: Option<Vec<String>>,
}
#[derive(Serialize, Deserialize, Clone, Debug, PartialEq, Eq)]
pub enum TlsVersion {
    TLS10,
    TLS11,
    TLS12,
    TLS13,
    NTLS,
    HigherThan13,
    Unknown,
}

impl Default for TlsVersion {
    fn default() -> Self {
        Self::Unknown
    }
}
impl<T> TlsHelperStream<T>
where
    T: AsyncRead + AsyncWrite + Unpin,
{
    pub fn new_buffered(inner: T) -> Self {
        Self {
            // 由于使用read_exact，为减少不必要的syscall，给reader添加缓存
            // writer使用0，避免写入延迟
            inner: BufStream::with_capacity(512, 0, inner),
            buf: smallvec::SmallVec::<[u8; 512]>::new(),
        }
    }
    pub async fn read_exact(&mut self, buf: &mut [u8], n: usize) -> anyhow::Result<usize> {
        let res = self.inner.read_exact(&mut buf[..n]).await?;
        self.buf.extend_from_slice(&buf[..res]);
        Ok(res)
    }
    /// parse sni from tls client hello record
    pub async fn sniff(&mut self) -> anyhow::Result<TlsRecord> {
        let task = async move {
            // https://tls12.xargs.org/#client-hello
            let mut buf = [0; 512];
            let mut record = TlsRecord {
                ..Default::default()
            };
            self.read_exact(&mut buf, 5).await?;
            if buf[0] != 0x16 {
                // not a handshake record
                bail!("unexpected handshake record type {}", buf[0]);
            }
            self.read_exact(&mut buf, 4).await?;
            if buf[0] != 1 {
                bail!("unexpected handshake type {}, expected: 0x16", buf[0]);
            }
            // client version
            self.read_exact(&mut buf, 2).await?;
            // 在1.3之前，1.2已经大规模部署，许多server并不支持大于1.2的版本号
            // 所以在1.3中，version写死为1.2，以此作为兼容
            // client hello中，tls 1.3 version在 Extension: supported_versions指定，表示client支持1.3版本
            // 如果协商采用1.3，则ServerHello也需要在supported_versions指定为1.3
            let version = (buf[0], buf[1]);
            match version {
                (0x03, 0x01) => {
                    // tls 1.0
                    record.version = Some(TlsVersion::TLS10)
                }
                (0x03, 0x02) => {
                    // tls 1.1
                    record.version = Some(TlsVersion::TLS11)
                }
                (0x03, 0x03) => {
                    // 1.2, maybe 1.3
                    record.version = Some(TlsVersion::TLS12)
                }
                (0x01, 0x01) => {
                    // ntls
                    record.version = Some(TlsVersion::NTLS)
                }
                _ => {
                    // other version
                }
            }
            // client random
            self.read_exact(&mut buf, 32).await?;
            // session id len
            self.read_exact(&mut buf, 1).await?;
            let len = buf[0] as usize;
            // session id
            self.read_exact(&mut buf, len).await?;
            // cipher suites len
            self.read_exact(&mut buf, 2).await?;
            let len_byte = &buf[..2];
            let len = u16::from_be_bytes(len_byte.try_into().unwrap()) as usize;
            // cipher suites
            self.read_exact(&mut buf, len).await?;
            record.suites = Some(self.parse_cipher_suites(&buf[..len]));
            // compression method len
            self.read_exact(&mut buf, 1).await?;
            let len = buf[0] as usize;
            // compression method
            self.read_exact(&mut buf, len).await?;
            // extensions length
            self.read_exact(&mut buf, 2).await?;
            let len_byte = &buf[..2];
            let len = u16::from_be_bytes(
                len_byte
                    .try_into()
                    .expect("buf len should 2 bytes as least"),
            ) as usize;
            // extensions
            self.read_exact(&mut buf, len).await?;
            // process extensions
            let mut exts_bytes = &buf[..len];
            while !exts_bytes.is_empty() {
                let ty = &exts_bytes[0..2];
                let mut curr_ext = cut(exts_bytes, 2..4).expect("should cut success");
                match [ty[0], ty[1]] {
                    [0x00, 0x00] => {
                        // server name indication
                        // sni
                        // RFC上允许多个server name https://www.rfc-editor.org/rfc/rfc6066#section-3
                        // 看了下rustls实现，只保留了第一个
                        curr_ext = &curr_ext[2..];
                        if curr_ext[0] == 0x00 {
                            // server name type: host_name
                            let host_len =
                                u16::from_be_bytes(curr_ext[1..3].try_into().unwrap()) as usize;
                            let s = std::str::from_utf8(&curr_ext[3..3 + host_len])?;
                            let final_name = SmallString::from_str(s)?;
                            record.sni = Some(final_name);
                        } else {
                            // unknown server name type
                            record.sni = None;
                        }
                    }
                    [0x00, 0x2b] => {
                        // supported_versions
                        // maybe tls1.3
                        let len = curr_ext[0] as usize;
                        curr_ext = &curr_ext[1..];
                        for _ in 0..len / 2 {
                            match [curr_ext[0], curr_ext[1]] {
                                [0xda, 0xda] => {
                                    // reserved
                                }
                                // we don't check tls1.2
                                // tls1.2 already set in above
                                // [0x03, 0x03] => {
                                //     record.version = Some(TlsVersion::TLS12);
                                // }
                                [0x03, 0x04] => {
                                    record.version = Some(TlsVersion::TLS13);
                                }
                                _ => {}
                            }
                            curr_ext = &curr_ext[2..]
                        }
                    }
                    _ => {}
                }
                exts_bytes = truncate_before(exts_bytes, 2..4).map_err(|err| anyhow!(err))?;
            }
            log::debug!("tls version {:?}", record.version);
            Ok(record)
        };
        tokio::time::timeout(SSL_READ_TIMEOUT, task).await?
    }

    fn parse_cipher_suites(&self, buf: &[u8]) -> Vec<CipherSuite> {
        let len = buf.len();
        let mut pos = 0;
        let mut suites = Vec::with_capacity(len / 2 + 1);
        while pos < len {
            let cipher = CipherSuite::from_hex(&[buf[pos], buf[pos + 1]]);
            pos += 2;
            suites.push(cipher);
        }
        suites
    }
    // read from head without remove from queue, like socket#MSG_PEEK
    pub async fn peek(&mut self, buf: &mut [u8]) -> anyhow::Result<()> {
        let len = buf.len();
        let len_in_buf = self.buf.len();
        if len_in_buf >= len {
            buf.copy_from_slice(&self.buf[..len]);
        } else {
            let remaining = len - len_in_buf;
            self.read_exact(&mut buf[len_in_buf..], remaining).await?;
            buf[..len_in_buf].copy_from_slice(&self.buf[..len_in_buf]);
        };
        Ok(())
    }
}

impl<T> AsyncRead for TlsHelperStream<T>
where
    T: AsyncRead + AsyncWrite + Unpin,
{
    fn poll_read(
        mut self: Pin<&mut Self>,
        cx: &mut Context<'_>,
        buf: &mut ReadBuf<'_>,
    ) -> Poll<io::Result<()>> {
        if !self.buf.is_empty() {
            let min_len = cmp::min(buf.remaining(), self.buf.len());
            buf.put_slice(&self.buf[..min_len]);
            self.buf.drain(..min_len);
            Poll::Ready(Ok(()))
        } else {
            Pin::new(&mut self.inner).poll_read(cx, buf)
        }
    }
}

impl<T> AsyncWrite for TlsHelperStream<T>
where
    T: AsyncRead + AsyncWrite + Unpin,
{
    fn poll_flush(mut self: Pin<&mut Self>, cx: &mut Context<'_>) -> Poll<Result<(), io::Error>> {
        Pin::new(&mut self.inner).poll_flush(cx)
    }
    fn poll_shutdown(
        mut self: Pin<&mut Self>,
        cx: &mut Context<'_>,
    ) -> Poll<Result<(), io::Error>> {
        Pin::new(&mut self.inner).poll_shutdown(cx)
    }
    fn poll_write(
        mut self: Pin<&mut Self>,
        cx: &mut Context<'_>,
        buf: &[u8],
    ) -> Poll<Result<usize, io::Error>> {
        Pin::new(&mut self.inner).poll_write(cx, buf)
    }
    fn poll_write_vectored(
        mut self: Pin<&mut Self>,
        cx: &mut Context<'_>,
        bufs: &[IoSlice<'_>],
    ) -> Poll<Result<usize, io::Error>> {
        Pin::new(&mut self.inner).poll_write_vectored(cx, bufs)
    }
    fn is_write_vectored(&self) -> bool {
        self.inner.is_write_vectored()
    }
}

pub(crate) struct Fake<'a> {
    pub(crate) data: &'a [u8],
}
impl<'a> AsyncRead for Fake<'a> {
    fn poll_read(
        mut self: Pin<&mut Self>,
        _cx: &mut Context<'_>,
        buf: &mut ReadBuf<'_>,
    ) -> Poll<io::Result<()>> {
        let me = &mut *self;
        let min_len = cmp::min(me.data.len(), buf.remaining());
        buf.put_slice(&me.data[..min_len]);
        me.data = &me.data[min_len..];
        Poll::Ready(Ok(()))
    }
}

impl<'a> AsyncWrite for Fake<'a> {
    fn poll_write(
        self: Pin<&mut Self>,
        _cx: &mut Context<'_>,
        buf: &[u8],
    ) -> Poll<io::Result<usize>> {
        Poll::Ready(Ok(buf.len()))
    }
    fn poll_flush(self: Pin<&mut Self>, _cx: &mut Context<'_>) -> Poll<io::Result<()>> {
        Poll::Ready(Ok(()))
    }
    fn poll_shutdown(self: Pin<&mut Self>, _cx: &mut Context<'_>) -> Poll<io::Result<()>> {
        Poll::Ready(Ok(()))
    }
}
